package practicas;

public class PracticaUno {
	
	public static boolean esPar(int num) {
		boolean esPar = false;
		if (num % 2 == 0) {
			esPar = true;
		}
		return esPar;
	}
	
	public static boolean esPositivo(int num) {
		boolean esPositivo = false;
		if (num > 0) {
			esPositivo = true;
		}
		return esPositivo;
	}
	

}
